def extract_social_links(platform_name, social_data) -> str:
        platform_urls = {
            'LINKEDIN': 'https://www.linkedin.com/in/',
            'SKYPE': 'unsafe:skype://',
            'YOUTUBE': 'https://www.youtube.com/',
            'INSTAGRAM': 'https://instagram.com/',
            'TWITTER': 'https://x.com/',
            'FACEBOOK': 'https://www.facebook.com/',
            'PINTEREST': 'https://pinterest.com/'
        }

        if platform_name not in platform_urls:
            print(f"Unsupported social media platform: {platform_name}")
            return None

        for social in social_data:
            if social.get('type') == platform_name:
                social_link = social.get('profile')

                if not social_link:
                    print(f"Missing or invalid profile for platform: {platform_name}")
                    return None

                base_url = platform_urls[platform_name]

                if platform_name == 'SKYPE':
                    if 'unsafe:' not in social_link:
                        return f'{base_url}{social_link}'
                    return social_link
                else:
                    if 'https:' not in social_link:
                        return f'{base_url}{social_link}'
                    return social_link

        return None
    
    
def field_extractions(field_name, event_data) -> str:
    try:

        for event in event_data:
            if event and event.get('name') == field_name:
                data = event.get('value')
                if isinstance(data, dict):
                    return event.get('value').get('text')
                else:
                    return ",".join([ii.get('text') for ii in event.get('values')])
    except: return None